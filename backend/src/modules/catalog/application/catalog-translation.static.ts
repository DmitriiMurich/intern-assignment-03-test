export function localizeCategoryTitle(
  translation: MissingCategoryTranslation,
  languageCode: TargetLanguageCode,
): string {
  return categoryTitleTranslations[translation.categorySlug]?.[languageCode] ?? translation.sourceTitle;
}

export function localizeProductTitle(
  translation: MissingProductTranslation,
  languageCode: TargetLanguageCode,
): string {
  return productTitleTranslations[translation.productId]?.[languageCode] ?? translation.sourceTitle;
}

export function localizeProductDescription(
  params: {
    productId: number;
    sourceDescription: string;
  },
  languageCode: TargetLanguageCode,
): string {
  const exactTranslation = getProductDescriptionTranslation(params.productId, languageCode);

  if (exactTranslation) {
    return exactTranslation;
  }

  return params.sourceDescription;
}
